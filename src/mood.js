const MOOD_RULES = [
  {
    mood: "upset",
    cues: ["груст", "плохо", "тоск", "устал", "больно", "одиноко", "депресс", "сложно", "тяжело", "ненавижу", "отстой"]
  },
  {
    mood: "angry",
    cues: ["бесит", "злюсь", "раздраж", "ненавижу", "достал", "тупо", "черт", "какого", "жесть"]
  },
  {
    mood: "anxious",
    cues: ["тревог", "пережив", "боюсь", "страшно", "паник", "волнуюсь", "не уверен", "что если"]
  },
  {
    mood: "playful",
    cues: ["ха-ха", "лол", "шут", "мем", "прикол", "смешно", ":)", ")))", "ахах"]
  },
  {
    mood: "romantic",
    cues: ["люблю", "влюб", "скучаю", "нежно", "обнять", "поцел", "сердце", "<3"]
  }
];

export function detectMood(text) {
  const normalized = String(text || "").toLowerCase();

  for (const rule of MOOD_RULES) {
    if (rule.cues.some((cue) => normalized.includes(cue))) {
      return rule.mood;
    }
  }

  if (normalized.includes("?")) {
    return "curious";
  }

  return "neutral";
}

export function buildMoodGuidance(mood) {
  const guidanceByMood = {
    upset: "Пользователь расстроен. Отвечай особенно мягко, поддерживающе, без насмешек.",
    angry: "Пользователь раздражен. Не спорь ради спора, снизь напряжение и говори спокойно.",
    anxious: "Пользователь тревожится. Дай ощущение опоры, ясности и спокойствия.",
    playful: "Пользователь в игривом настроении. Можно быть живее, остроумнее и легче.",
    romantic: "Пользователь настроен нежно или романтично. Отвечай деликатно и тепло.",
    curious: "Пользователь задает вопрос и ждет понятного ответа. Будь ясным и вовлеченным.",
    neutral: "Пользователь в нейтральном настроении. Сохраняй характер и естественный тон."
  };

  return guidanceByMood[mood] ?? guidanceByMood.neutral;
}
