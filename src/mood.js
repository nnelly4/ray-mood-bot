const MOOD_RULES = [
  {
    mood: "upset",
    cues: ["груст", "плохо", "тоск", "устал", "больно", "одиноко", "депресс", "сложно", "тяжело"]
  },
  {
    mood: "anxious",
    cues: ["тревог", "пережив", "боюсь", "страшно", "паник", "волнуюсь", "не уверен", "что если"]
  },
  {
    mood: "playful",
    cues: ["ха-ха", "лол", "шут", "мем", "прикол", "смешно", ":)", ")))", "ахах", "ржу", "угар"]
  },
  {
    mood: "banter",
    cues: ["черт", "нахуй", "еблан", "долбоеб", "мраз", "пидорас", "сука", "иди ты", "я твою маму", "хер", "пошел"]
  },
  {
    mood: "angry",
    cues: ["бесит", "злюсь", "раздраж", "ненавижу", "достал", "тупо", "какого", "жесть"]
  },
  {
    mood: "romantic",
    cues: ["люблю", "влюб", "скучаю", "нежно", "обнять", "поцел", "сердце", "<3"]
  }
];

export function detectMood(text) {
  const normalized = String(text || "").toLowerCase();

  for (const rule of MOOD_RULES) {
    if (rule.cues.some((cue) => normalized.includes(cue))) {
      return rule.mood;
    }
  }

  if (normalized.includes("?")) {
    return "curious";
  }

  return "neutral";
}

